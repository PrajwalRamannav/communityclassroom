package com.prajwal;

public class DataTypes {
    public static void main(String[] args) {
        // Data types are classified as -> 1 - Primitive Data Type
        //                              -> 2 - Non-Primitive Data Type

        // Primitive Data Type :
        // These are the data types which cannot be broken even into further data types
        // Ex:- String = "prajwal"; ==> char 'p', 'r', 'a', 'j', 'w', 'a', 'l'
        //      but int = 10; ==> cannot be broken into further data type

        // List of Primitive data types :
        // 1-> integer -> int = 10;
        // 2-> character -> char = 'A';
        // 3-> float -> float = 25.24f;
        // 4-> double -> double = 25.252525;
        // 5-> long -> long = 1234567889L;
        // 6-> boolean -> boolean = true; & boolean = false;


    }
}
