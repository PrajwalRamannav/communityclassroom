package com.easy;

public class PerimeterOfRectangle {
    public static void main(String[] args) {
        // Perimeter Of Rectangle : p = 2 (l + w) => l =length and w = width;
        int length = 100;
        int width = 50;
        int perimeter = 2 * (length * width);
        System.out.println(perimeter);
    }
}
