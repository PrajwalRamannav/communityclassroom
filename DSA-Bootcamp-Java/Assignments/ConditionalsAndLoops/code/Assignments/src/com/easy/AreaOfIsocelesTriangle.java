package com.easy;

public class AreaOfIsocelesTriangle {
    public static void main(String[] args) {
        // Area Of Isosceles Triangle : A = (B * Hb) / 2
        int base = 10;
        int heightOfBase = 200;
        int area = (base * heightOfBase) / 2;
        System.out.println(area);
    }
}
